#정수형
a = 10
b = -10
c = 0

print(a)
print(b)
print(c)

#실수형
a = 3.14
b = -3.14

print(a)
print(b)

#8진수와 16진수
a = 0o13 #8진수는 0o로 시작
b = 0xb  #16진수는 0x로 시작

print(a)
print(b)

#사칙연산
a = 10
b = 5
add = a + b
sub = a - b
mul = a * b
div = a / b

print(add)
print(sub)
print(mul)
print(div)

#제곱 연산자 **
a = 4
b = 2
c = a ** b

print(c)

#나머지 반환 연산자 %
a = 7
b = 3
c = 7 % 3

print(c)

#몫 반환 연산자 //
a = 7
b = 3
c = a // b

print(c)